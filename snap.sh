#!/usr/bin/env bash

# Install Snap Daemon
sudo apt install snapd

# Install snaps
sudo snap install intellij-idea-community --classic
sudo snap install code --classic
sudo snap install slack --classic
sudo snap install shfmt            # A shell parser, formatter and interpreter (POSIX/Bash/mksh) (https://github.com/mvdan/sh)
sudo snap install remmina          # Remote Desktop Client (https://remmina.org)
sudo snap install telegram-desktop # Official desktop client for the Telegram messenger (https://desktop.telegram.org)
sudo snap install foliate          # A simple and modern eBook viewer (https://johnfactotum.github.io/foliate)
sudo snap install plexmediaserver  # Plex magically organizes your media libraries and streams them to any device (https://www.plex.tv)
sudo snap install spotify          # Music for everyone (https://www.spotify.com)
sudo snap install ncspot           # Cross-platform ncurses Spotify client written in Rust (https://github.com/hrkfdn/ncspot)
sudo snap install musixmatch       # Musixmatch gives you the lyrics of your favorite songs (https://www.musixmatch.com)
sudo snap install simplenote       # The simplest way to keep notes (https://github.com/snapcrafters/simplenote)(https://simplenote.com/)
sudo snap install inkscape         # Vector Graphics Editor (https://inkscape.org)
sudo snap install gimp             # The Free & Open Source Image Editor (https://www.gimp.org)
sudo snap install krita            # Digital Painting, Creative Freedom (https://krita.org)
